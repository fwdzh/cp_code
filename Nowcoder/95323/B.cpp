void solve()
{
    int n;
    cin>>n;
    vector<vi>adj(n+1,vi());
    for(int i=1;i<n;i++){
        int u,v;
        cin>>u>>v;
        adj[u].push_back(v);
        adj[v].push_back(u);
    }
    int cnt=0;
    for(int i=1;i<=n;i++){
        if(sz(adj[i])==1) cnt++;
    }
    if(cnt>2) cout<<"-1\n";
    else{
        for(int i=1;i<=n;i++){
            if(sz(adj[i])==1)
                cout<<i<<" ";
        }
    }
}

