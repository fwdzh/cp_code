void solve()
{
    //10101 有多少个 就是多少
    int n;
    cin>>n;
    string s;
    cin>>s;
    int lst=1;
    s=" "+s;
    int ans=0;
    for(int i=1;i<=n;i++){
        if(s[i]!=s[i-1]&&s[i]-'0'==lst){
            ans++;
            lst^=1;
        }
    }
    cout<<ans<<'\n';
}

